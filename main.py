# Nombre maximal d'astérix imprimés
nombre_de_prints = 11

# Imprimer une ligne d'astérix
# À chaque reprise, on diminue le nombre de prints par 1
while nombre_de_prints > 0:
    print('*' * nombre_de_prints)
    nombre_de_prints -= 1
    
